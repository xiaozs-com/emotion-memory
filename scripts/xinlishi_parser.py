def get_scale_info(xinlishi_id):
    """根据xinlishi量表ID查找量表信息"""
    info = ALL_SCALES.get(str(xinlishi_id))
    if info:
        return {"xinlishi_id": xinlishi_id, "name": info[0], "scale_id": info[1], "category": info[2]}
    return None


def guess_scale_from_text(text):
    """根据用户文本猜测是哪个量表"""
    text_lower = text.lower()
    for keyword, xinlishi_id in KEYWORD_MAP.items():
        if keyword.lower() in text_lower:
            return get_scale_info(xinlishi_id)
    return None


